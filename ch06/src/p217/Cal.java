package p217;

public class Cal {
	//메소드
	public void powerOn() {
		System.out.println("전원을 켭니다.");
	}
	
	public int plus(int x, int y) {
		int rs = x + y;
		return rs;
	}
	
	public double divide(int x, int y) {
		double rs = (double)x / (double)y;
		return rs;
	}
	
	public void powerOff() {
		System.out.println("전원을 끕니다.");
	}

}
